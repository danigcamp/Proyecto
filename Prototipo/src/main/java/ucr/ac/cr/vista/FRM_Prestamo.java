    public void llenarComboLibros(ArrayList<Libro> listaLibrosPrestados) {
   
        for (int i = 0; i < listaLibrosPrestados.size(); i++) {
            Libro libro = listaLibrosPrestados.get(i);
            comboLibros.addItem(i+"."+libro.getTitulo());
        }

    }
